test('uses canonical destination identity through symlinked parent directories', async () => {
  const root = await copyFixture()
  await symlink(join(root, 'queries'), join(root, 'query-link'))
  const config = {
    include: ['queries', 'query-link'],
    rootDir: root,
    scalarProfile: 'node-postgres' as const,
    schema: 'schema.sql',
    typesOutput: 'query-link/catalog.typed-sql.ts',
  }

  const first = await generateTypedSql(config)
  assert.equal(first.statementCount, 4)
  const stalePath = join(root, 'queries/stale.typed-sql.ts')
  await writeFile(stalePath, `${generatedMarker}\nexport const stale = true\n`)
  const second = await generateTypedSql(config)
  assert.equal(second.statementCount, 4)
  assert.equal(second.removedFiles, 1)
  await assert.rejects(readFile(stalePath, 'utf8'), /ENOENT/u)
  const third = await generateTypedSql(config)
  assert.equal(third.removedFiles, 0)
  assert.match(await readFile(join(root, 'queries/catalog.typed-sql.ts'), 'utf8'), /generated by postgres-typed-sql/u)

  const collisionRoot = await copyFixture()
  await symlink(join(collisionRoot, 'queries'), join(collisionRoot, 'query-link'))
  await assert.rejects(
    generateTypedSql({
      include: ['queries'],
      rootDir: collisionRoot,
      scalarProfile: 'node-postgres',
      schema: 'schema.sql',
      typesOutput: 'query-link/find-account-by-email.typed-sql.ts',
    }),
    /same filesystem destination.*claimed more than once/u
  )
})

test('uses the destination filesystem rather than the host platform for case identity', async () => {
  const root = await copyFixture()
  const probe = join(root, 'CaseProbe')
  await writeFile(probe, 'probe')
  let caseSensitive = false
  try {
    await readFile(join(root, 'caseprobe'), 'utf8')
  } catch (error) {
    if (error instanceof Error && 'code' in error && error.code === 'ENOENT') {
      caseSensitive = true
    } else {
      throw error
    }
  }

  const generation = generateTypedSql({
    include: ['queries'],
    rootDir: root,
    scalarProfile: 'node-postgres',
    schema: 'schema.sql',
    typesOutput: 'queries/FIND-ACCOUNT-BY-EMAIL.typed-sql.ts',
  })
  if (caseSensitive) {
    await generation
    assert.match(
      await readFile(join(root, 'queries/FIND-ACCOUNT-BY-EMAIL.typed-sql.ts'), 'utf8'),
      /export interface CatalogDb/u
    )
    assert.match(
      await readFile(join(root, 'queries/find-account-by-email.typed-sql.ts'), 'utf8'),
      /createTypedSqlStatement/u
    )
  } else {
    await assert.rejects(generation, /same filesystem destination.*claimed more than once/u)
  }
})

test('catalog output symlinks are rejected before any generated output is mutated', async () => {
  const root = await copyFixture()
  const catalogPath = join(root, 'postgres-typed-sql.types.ts')
  const catalogTargetPath = join(root, 'catalog-target.ts')
  const targetContents = 'export const catalogTarget = true\n'
  await writeFile(catalogTargetPath, targetContents)
  await symlink(catalogTargetPath, catalogPath)

  await assert.rejects(
    generate(root),
    /Generated output path .*postgres-typed-sql\.types\.ts must not be a symbolic link/u
  )
  assert.equal((await lstat(catalogPath)).isSymbolicLink(), true)
  assert.equal(await readFile(catalogTargetPath, 'utf8'), targetContents)
  await assert.rejects(
    readFile(join(root, 'queries/find-account-by-email.typed-sql.ts'), 'utf8'),
    (error: unknown) => error instanceof Error && 'code' in error && error.code === 'ENOENT'
  )
})

test('statement output symlinks are rejected before catalog or sibling outputs are mutated', async () => {
  const root = await copyFixture()
  const catalogPath = join(root, 'postgres-typed-sql.types.ts')
  const catalogContents = 'export const existingCatalog = true\n'
  const statementPath = join(root, 'queries/find-account-by-email.typed-sql.ts')
  const statementTargetPath = join(root, 'statement-target.ts')
  const targetContents = 'export const statementTarget = true\n'
  await writeFile(catalogPath, catalogContents)
  await writeFile(statementTargetPath, targetContents)
  await symlink(statementTargetPath, statementPath)

  await assert.rejects(
    generate(root),
    /Generated output path .*find-account-by-email\.typed-sql\.ts must not be a symbolic link/u
  )
  assert.equal(await readFile(catalogPath, 'utf8'), catalogContents)
  assert.equal((await lstat(statementPath)).isSymbolicLink(), true)
  assert.equal(await readFile(statementTargetPath, 'utf8'), targetContents)
  await assert.rejects(
    readFile(join(root, 'queries/list-accounts-with-posts.typed-sql.ts'), 'utf8'),
    (error: unknown) => error instanceof Error && 'code' in error && error.code === 'ENOENT'
  )
  const stagingArtifacts = [...(await readdir(root)), ...(await readdir(join(root, 'queries')))].filter(
    (entry) => entry.endsWith('.staged') || entry.endsWith('.backup')
  )
  assert.deepEqual(stagingArtifacts, [])
})
