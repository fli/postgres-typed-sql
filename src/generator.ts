function typedSqlCardinalityFromRowBounds(bounds: TypedSqlPostgresIrRowBounds): TypedSqlCardinality {
  if (bounds.min === 0 && bounds.max === 0) {
    return 'none'
  }
  if (bounds.min === 1 && bounds.max === 1) {
    return 'one'
  }
  if (bounds.min === 0 && bounds.max === 1) {
    return 'optional'
  }
  return 'many'
}

