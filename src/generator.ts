type ResolvedJsonLeafShape<Kind extends 'opaque' | 'scalar' | 'stringLiteral'> = Omit<
  Extract<TypedSqlPostgresIrJsonShape, { readonly kind: Kind }>,
  'nullability'
> & {
  readonly nullable: boolean
}

type ResolvedJsonShape =
  | {
      readonly element: ResolvedJsonShape
      readonly kind: 'array'
      readonly nullable: boolean
    }
  | {
      readonly fields: readonly ResolvedJsonField[]
      readonly kind: 'object'
      readonly nullable: boolean
    }
  | ResolvedJsonLeafShape<'opaque'>
  | ResolvedJsonLeafShape<'scalar'>
  | ResolvedJsonLeafShape<'stringLiteral'>
  | {
      readonly alternatives: readonly ResolvedJsonShape[]
      readonly kind: 'union'
      readonly nullable: boolean
    }

interface ResolvedJsonField {
  readonly name: string
  readonly propertyName: string
  readonly shape: ResolvedJsonShape
}

interface GeneratedJsonFieldMapping {
  readonly mapping?: GeneratedJsonMapping
  readonly name: string
  readonly propertyName: string
}

interface GeneratedJsonMapping {
  readonly arrayElement?: GeneratedJsonMapping
  readonly fields?: readonly GeneratedJsonFieldMapping[]
}

function propertyNameForNaming(name: string, naming: PostgresTypedSqlPropertyNaming): string {
  return naming === 'camelCase' ? camelCasePropertyName(name) : name
}

function resolveJsonShapeNames(
  shape: TypedSqlPostgresIrJsonShape,
  naming: PostgresTypedSqlPropertyNaming,
  sourceFile: string,
  context: string
): ResolvedJsonShape {
  switch (shape.kind) {
    case 'array':
      return {
        element: resolveJsonShapeNames(shape.element, naming, sourceFile, `${context}[]`),
        kind: 'array',
        nullable: resultNullabilityAllowsNull(shape.nullability),
      }
    case 'object': {
      const fields = shape.fields.map((field) => ({
        name: field.name,
        propertyName: propertyNameForNaming(field.name, naming),
        shape: resolveJsonShapeNames(field.shape, naming, sourceFile, `${context}.${field.name}`),
      }))
      assertUniquePublicNames(
        fields.map((field) => field.propertyName),
        `JSON field in ${context}`,
        sourceFile
      )
      return {
        fields,
        kind: 'object',
        nullable: resultNullabilityAllowsNull(shape.nullability),
      }
    }
    case 'opaque':
    case 'scalar':
    case 'stringLiteral': {
      const { nullability, ...value } = shape
      return {
        ...value,
        nullable: resultNullabilityAllowsNull(nullability),
      }
    }
    case 'union':
      return {
        alternatives: shape.alternatives.map((alternative, index) =>
          resolveJsonShapeNames(alternative, naming, sourceFile, `${context}<alternative ${index + 1}>`)
        ),
        kind: 'union',
        nullable: resultNullabilityAllowsNull(shape.nullability),
      }
  }
}

function isJsonTraversalBarrier(shape: TypedSqlPostgresIrJsonShape): boolean {
  return shape.kind === 'opaque' || (shape.kind === 'scalar' && postgresJsonValueMayBeStructured(shape))
}

function applyOpaqueJsonBarriersToAlternatives(
  shapes: readonly TypedSqlPostgresIrJsonShape[]
): readonly TypedSqlPostgresIrJsonShape[] {
  if (shapes.length === 0) {
    return shapes
  }
  const alternatives = shapes.flatMap(flattenJsonShapeAlternatives)
  if (alternatives.some(isJsonTraversalBarrier)) {
    return shapes.map((shape) => ({ kind: 'opaque', nullability: shape.nullability }))
  }

  const arrayElements = alternatives.flatMap((alternative) =>
    alternative.kind === 'array' ? [alternative.element] : []
  )
  const safeArrayElements = applyOpaqueJsonBarriersToAlternatives(arrayElements)
  let arrayIndex = 0

  const objectFields = new Map<string, TypedSqlPostgresIrJsonField[]>()
  for (const alternative of alternatives) {
    if (alternative.kind !== 'object') {
      continue
    }
    for (const field of alternative.fields) {
      const fields = objectFields.get(field.name) ?? []
      fields.push(field)
      objectFields.set(field.name, fields)
    }
  }
  const safeObjectFieldShapes = new Map(
    [...objectFields].map(([name, fields]) => [
      name,
      applyOpaqueJsonBarriersToAlternatives(fields.map((field) => field.shape)),
    ])
  )
  const objectFieldIndexes = new Map<string, number>()

  const rewrite = (shape: TypedSqlPostgresIrJsonShape): TypedSqlPostgresIrJsonShape => {
    switch (shape.kind) {
      case 'array': {
        const element = safeArrayElements[arrayIndex]
        arrayIndex += 1
        return { ...shape, element: element ?? shape.element }
      }
      case 'object':
        return {
          ...shape,
          fields: shape.fields.map((field) => {
            const fieldIndex = objectFieldIndexes.get(field.name) ?? 0
            objectFieldIndexes.set(field.name, fieldIndex + 1)
            return {
              ...field,
              shape: safeObjectFieldShapes.get(field.name)?.[fieldIndex] ?? field.shape,
            }
          }),
        }
      case 'union':
        return { ...shape, alternatives: shape.alternatives.map(rewrite) }
      case 'opaque':
      case 'scalar':
      case 'stringLiteral':
        return shape
    }
  }

  return shapes.map(rewrite)
}

function applyOpaqueJsonBarriers(shape: TypedSqlPostgresIrJsonShape): TypedSqlPostgresIrJsonShape {
  return applyOpaqueJsonBarriersToAlternatives([shape])[0] ?? shape
}

function flattenResolvedJsonAlternatives(shape: ResolvedJsonShape): readonly ResolvedJsonShape[] {
  return shape.kind === 'union' ? shape.alternatives.flatMap(flattenResolvedJsonAlternatives) : [shape]
}

function jsonMappingForAlternatives(shapes: readonly ResolvedJsonShape[]): GeneratedJsonMapping | undefined {
  const alternatives = shapes.flatMap(flattenResolvedJsonAlternatives)
  if (alternatives.some((alternative) => alternative.kind === 'opaque')) {
    return undefined
  }

  const elementShapes = alternatives.flatMap((alternative) =>
    alternative.kind === 'array' ? [alternative.element] : []
  )
  const arrayElement = elementShapes.length > 0 ? jsonMappingForAlternatives(elementShapes) : undefined

  const fieldsByName = new Map<string, { propertyName: string; shapes: ResolvedJsonShape[] }>()
  for (const alternative of alternatives) {
    if (alternative.kind !== 'object') {
      continue
    }
    for (const field of alternative.fields) {
      const existing = fieldsByName.get(field.name)
      if (existing) {
        if (existing.propertyName !== field.propertyName) {
          throw new Error(`Inconsistent mapped property name for JSON field ${JSON.stringify(field.name)}.`)
        }
        existing.shapes.push(field.shape)
      } else {
        fieldsByName.set(field.name, { propertyName: field.propertyName, shapes: [field.shape] })
      }
    }
  }
  const fields = [...fieldsByName].flatMap(
    ([name, { propertyName, shapes: fieldShapes }]): GeneratedJsonFieldMapping[] => {
      const mapping = jsonMappingForAlternatives(fieldShapes)
      return propertyName !== name || mapping
        ? [
            {
              ...(mapping ? { mapping } : {}),
              name,
              propertyName,
            },
          ]
        : []
    }
  )
  return arrayElement || fields.length > 0
    ? {
        ...(arrayElement ? { arrayElement } : {}),
        ...(fields.length > 0 ? { fields } : {}),
      }
    : undefined
}

function jsonMappingForShape(shape: ResolvedJsonShape): GeneratedJsonMapping | undefined {
  return jsonMappingForAlternatives([shape])
}

function assertUniquePublicNames(names: readonly string[], kind: string, sourceFile: string): void {
  const firstIndexByName = new Map<string, number>()
  for (const [index, name] of names.entries()) {
    const firstIndex = firstIndexByName.get(name)
    if (firstIndex !== undefined) {
      throw new Error(
        `${sourceFile}: duplicate ${kind} name ${JSON.stringify(name)} at positions ${firstIndex + 1} and ${index + 1}.`
      )
    }
    firstIndexByName.set(name, index)
  }
}

function reserveGeneratedTypeBinding(
  bindings: Map<string, string>,
  name: string,
  source: string,
  sourceFile: string
): void {
  assertTypeScriptBindingIdentifier(name, `${sourceFile}: generated type for ${source}`)
  const firstSource = bindings.get(name)
  if (firstSource) {
    throw new Error(
      `${sourceFile}: generated TypeScript binding ${name} for ${source} collides with ${firstSource}. Alias the SQL field to give it a distinct name.`
    )
  }
  bindings.set(name, source)
}

function encodedTypeNameSegment(value: string): string {
  let encoded = ''
  for (const character of value) {
    encoded += /^[A-Za-z0-9_]$/u.test(character) ? character : `$${character.codePointAt(0)?.toString(16) ?? ''}$`
  }
  return `J${encoded.length}_${encoded}`
}

function scalarTsTypeForJsonShape(
  shape: Extract<ResolvedJsonShape, { readonly kind: 'scalar' }>,
  dependencies: TypeScriptDependencies,
  codecProfile: ResolvedPostgresCodecProfile
): string {
  if (shape.checkConstraintType && postgresJsonSupportsStringLiteralRefinement(shape, codecProfile)) {
    return tsTypeForCheckConstraintType(shape.checkConstraintType)
  }

  const resolved = resolveTypeScriptJsonScalarTypeForPostgresType(shape, codecProfile)
  collectTypeResolutionDependencies(dependencies, resolved)
  return resolved.type
}

function tsTypeForJsonShape(
  shape: ResolvedJsonShape,
  sourceFile: string,
  suggestedName: string,
  declarations: GeneratedJsonTypeDeclaration[],
  usedNames: Map<string, string>,
  dependencies: TypeScriptDependencies,
  codecProfile: ResolvedPostgresCodecProfile
): string {
  switch (shape.kind) {
    case 'array':
      return `readonly (${tsTypeForJsonShape(
        shape.element,
        sourceFile,
        `${suggestedName}${encodedTypeNameSegment('element')}`,
        declarations,
        usedNames,
        dependencies,
        codecProfile
      )}${shape.element.nullable ? ' | null' : ''})[]`
    case 'object': {
      reserveGeneratedTypeBinding(usedNames, suggestedName, `JSON object ${suggestedName}`, sourceFile)
      assertUniquePublicNames(
        shape.fields.map((field) => field.propertyName),
        `JSON field in ${suggestedName}`,
        sourceFile
      )
      const name = suggestedName
      declarations.push({
        fields: shape.fields.map((field) => ({
          nullable: field.shape.nullable,
          propertyName: field.propertyName,
          tsType: tsTypeForJsonShape(
            field.shape,
            sourceFile,
            `${name}${encodedTypeNameSegment(field.name)}`,
            declarations,
            usedNames,
            dependencies,
            codecProfile
          ),
        })),
        name,
      })
      return name
    }
    case 'opaque':
      collectTypeResolutionDependencies(dependencies, codecProfile.opaqueJsonType)
      return codecProfile.opaqueJsonType.type
    case 'union':
      return shape.alternatives
        .map((alternative, index) =>
          tsTypeForJsonShape(
            alternative,
            sourceFile,
            `${suggestedName}${encodedTypeNameSegment(`alternative${index + 1}`)}`,
            declarations,
            usedNames,
            dependencies,
            codecProfile
          )
        )
        .join(' | ')
    case 'scalar':
      return scalarTsTypeForJsonShape(shape, dependencies, codecProfile)
    case 'stringLiteral': {
      if (postgresJsonSupportsStringLiteralRefinement(shape, codecProfile)) {
        return quoteString(shape.value)
      }
      const resolved = resolveTypeScriptJsonScalarTypeForPostgresType(shape, codecProfile)
      collectTypeResolutionDependencies(dependencies, resolved)
      return resolved.type
    }
  }
}

function renderImports(configs: readonly ResolvedTypedSql[], generatorConfig: ResolvedPostgresTypedSqlConfig): string {
  const scalarImports = new Set<string>()
  for (const config of configs) {
    for (const identifier of config.scalarTypeImports) {
      scalarImports.add(identifier)
    }
  }

  const lines: string[] = []
  if (scalarImports.size > 0) {
    lines.push(
      `import type { ${[...scalarImports].toSorted().join(', ')} } from ${quoteString(generatorConfig.imports.scalars)}`
    )
  }
  lines.push(`import { createTypedSqlStatement } from ${quoteString(generatorConfig.imports.runtime)}`)

  return lines.join('\n')
}

function renderInterface(
  name: string,
  fields: readonly (
    | Pick<ResolvedSqlColumn, 'nullable' | 'propertyName' | 'tsType'>
    | Pick<ResolvedSqlParameter, 'nullable' | 'propertyName' | 'tsType'>
  )[]
): string {
  if (fields.length === 0) {
    return `export type ${name} = Record<string, never>`
  }

  return [
    `export interface ${name} {`,
    ...fields.map(
      (field) =>
        `  readonly ${quotePropertyName(field.propertyName)}: ${field.tsType}${'nullable' in field && field.nullable ? ' | null' : ''}`
    ),
    '}',
  ].join('\n')
}

function renderGeneratedTypeDeclaration(declaration: GeneratedJsonTypeDeclaration): string {
  return renderInterface(declaration.name, declaration.fields)
}

function renderColumnSourceMetadata(source: TypedSqlPostgresIrColumnSource): string {
  if (source.kind !== 'tableColumn') {
    return JSON.stringify(source)
  }

  const stableSource = {
    attname: source.attname,
    kind: source.kind,
    relname: source.relname,
    varlevelsup: source.varlevelsup,
    varno: source.varno,
    varnullingrels: source.varnullingrels,
  }
  return JSON.stringify(stableSource)
}

function renderColumnMetadata(columns: readonly ResolvedSqlColumn[]): string {
  if (columns.length === 0) {
    return '[]'
  }

  return `[
${columns
  .map(
    (column) => `    {
      ${column.jsonMapping ? `jsonMapping: ${JSON.stringify(column.jsonMapping)},\n      ` : ''}name: ${quoteString(column.name)},
      nullable: ${column.nullable},
      pgType: ${quoteString(column.pgType)},
      pgTypeName: ${quoteString(column.pgTypeName)},
      pgTypeSchema: ${quoteString(column.pgTypeSchema)},
      propertyName: ${quoteString(column.propertyName)}${
        column.source ? `,\n      source: ${renderColumnSourceMetadata(column.source)}` : ''
      },
    }`
  )
  .join(',\n')},
  ]`
}

function renderParameterMetadata(parameters: readonly ResolvedSqlParameter[]): string {
  if (parameters.length === 0) {
    return '[]'
  }

  return `[
${parameters
  .map(
    (parameter) => `    {
      name: ${quoteString(parameter.name)},
      nullable: ${parameter.nullable},
      pgType: ${quoteString(parameter.pgType)},
      pgTypeName: ${quoteString(parameter.pgTypeName)},
      pgTypeSchema: ${quoteString(parameter.pgTypeSchema)},
      propertyName: ${quoteString(parameter.propertyName)},
    }`
  )
  .join(',\n')},
  ]`
}

function renderStatement(config: ResolvedTypedSql): string {
  const paramsName = `${pascalCaseIdentifier(config.name)}Params`
  const rowName = `${pascalCaseIdentifier(config.name)}Row`
  const parameterNames = config.parameters.map((parameter) => quoteString(parameter.propertyName)).join(', ')
  const generatedTypeDeclarations =
    config.generatedTypeDeclarations.length > 0 ? `${config.generatedTypeDeclarations.join('\n\n')}\n\n` : ''

  return `${generatedTypeDeclarations}${renderInterface(paramsName, config.parameters)}

${renderInterface(rowName, config.columns)}

export const ${config.name} = createTypedSqlStatement<
  ${paramsName},
  ${rowName},
  ${quoteString(config.access)},
  ${quoteString(config.cardinality)}
>({
  access: ${quoteString(config.access)},
  columns: ${renderColumnMetadata(config.columns)},
  command: ${quoteString(config.command)},
  cardinality: ${quoteString(config.cardinality)},
  name: ${quoteString(config.name)},
  parameterNames: [${parameterNames}],
  parameters: ${renderParameterMetadata(config.parameters)},
  rowBounds: { min: ${config.rowBounds.min}, max: ${config.rowBounds.max ?? 'null'}, proof: ${quoteString(config.rowBounds.proof)} },
  text: ${quoteString(config.sql)},
})`
}

function renderTypedSql(config: ResolvedTypedSql, generatorConfig: ResolvedPostgresTypedSqlConfig): string {
  const sourcePath = renderTypeScriptLineCommentValue(`./${basename(config.sourcePath)}`)
  const codecProfileName = renderTypeScriptLineCommentValue(generatorConfig.codecProfile.name)
  return `// ${generatedByMarker}
// Source: ${sourcePath}
// Codec profile: ${codecProfileName}
// DO NOT EDIT MANUALLY

${renderImports([config], generatorConfig)}

${renderStatement(config)}
`
}

async function assertColumnAssertions(
  client: PostgresQueryable,
  config: CompiledTypedSql,
  resolvedColumns: readonly ResolvedSqlColumn[]
): Promise<void> {
  if (config.columns.length === 0) {
    return
  }

  const resolvedByName = new Map(resolvedColumns.map((column) => [column.name, column]))
  const typedAssertions: { readonly assertion: SqlColumn; readonly resolved: ResolvedSqlColumn }[] = []
  for (const assertion of config.columns) {
    const resolved = resolvedByName.get(assertion.name)
    if (!resolved) {
      throw new Error(`${config.sourceFile}: @column ${assertion.name} does not exist in the PostgreSQL result.`)
    }

    if (assertion.pgType) {
      typedAssertions.push({ assertion, resolved })
    }
  }

  if (typedAssertions.length === 0) {
    return
  }

  const result = await client.query<{ readonly oid: number | null; readonly ordinal: number }>(
    `
      select
        asserted.ordinality::int as ordinal,
        to_regtype(asserted.pg_type)::oid::int as oid
      from unnest($1::text[]) with ordinality as asserted(pg_type, ordinality)
    `,
    [typedAssertions.map(({ assertion }) => assertion.pgType)]
  )
  const oidByOrdinal = new Map(result.rows.map((row) => [row.ordinal, row.oid]))
  for (const [index, { assertion, resolved }] of typedAssertions.entries()) {
    const assertedOid = oidByOrdinal.get(index + 1)
    if (!assertedOid) {
      throw new Error(`${config.sourceFile}: unknown @column type ${assertion.pgType} for ${assertion.name}.`)
    }
    if (assertedOid !== resolved.pgTypeOid) {
      throw new Error(
        `${config.sourceFile}: result column ${assertion.name} type drift. Expected ${assertion.pgType}, PostgreSQL inferred ${resolved.pgType}.`
      )
    }
  }
}

async function resolveTypedSqlWithAnalyzer(
  configs: readonly CompiledTypedSql[],
  client: PostgresQueryable,
  generatorConfig: ResolvedPostgresTypedSqlConfig
): Promise<ResolvedTypedSql[]> {
  const result = await buildTypedSqlPostgresIrFromCompiledConfigs(
    client,
    configs.map((config) => ({
      name: config.name,
      parameterNames: config.parameters.map((parameter) => parameter.name),
      parameterTypes: config.parameters.map((parameter) => parameter.pgType),
      sourceFile: config.sourceFile,
      sql: config.sql,
    }))
  )

  const resolvedQueries: ResolvedTypedSql[] = []
  for (const [index, ir] of result.queries.entries()) {
    const config = configs[index]
    if (!config) {
      throw new Error(`PostgreSQL analyzer returned an unexpected extra query ${ir.name}.`)
    }

    const generatedDeclarations: GeneratedJsonTypeDeclaration[] = []
    const typeDependencies: TypeScriptDependencies = {
      ambient: new Set(),
      scalar: new Set(),
    }
    const statementTypeBindings: TypeScriptBinding[] = [
      { name: `${pascalCaseIdentifier(config.name)}Params`, source: 'parameter interface' },
      { name: `${pascalCaseIdentifier(config.name)}Row`, source: 'row interface' },
    ]
    assertUniqueTypeScriptBindings(statementTypeBindings, config.sourceFile)
    const usedGeneratedTypeNames = new Map(statementTypeBindings.map((binding) => [binding.name, binding.source]))
    const columns = ir.resultColumns.map((column, columnIndex): ResolvedSqlColumn => {
      const name = column.name ?? `column_${columnIndex + 1}`
      const propertyName = propertyNameForNaming(name, generatorConfig.naming.resultColumns)
      const suggestedJsonTypeName = `${pascalCaseIdentifier(config.name)}${encodedTypeNameSegment(name)}Json`
      const safeJsonShape = column.jsonShape ? applyOpaqueJsonBarriers(column.jsonShape) : undefined
      const resolvedJsonShape = safeJsonShape
        ? resolveJsonShapeNames(
            safeJsonShape,
            generatorConfig.naming.structuredJsonFields,
            config.sourceFile,
            `result column ${JSON.stringify(name)}`
          )
        : undefined
      const jsonMapping = resolvedJsonShape ? jsonMappingForShape(resolvedJsonShape) : undefined
      if (jsonMapping && !generatorConfig.codecProfile.structuredJson) {
        throw new Error(
          `${config.sourceFile}: structured JSON field naming for result column ${JSON.stringify(name)} requires a codec profile that decodes structured JSON.`
        )
      }
      const jsonShape = generatorConfig.codecProfile.structuredJson ? resolvedJsonShape : undefined
      let tsType: string
      if (jsonShape) {
        tsType = tsTypeForJsonShape(
          jsonShape,
          config.sourceFile,
          suggestedJsonTypeName,
          generatedDeclarations,
          usedGeneratedTypeNames,
          typeDependencies,
          generatorConfig.codecProfile
        )
      } else {
        if (
          column.checkConstraintType &&
          postgresResultSupportsStringLiteralRefinement(column, generatorConfig.codecProfile)
        ) {
          tsType = tsTypeForCheckConstraintType(column.checkConstraintType)
        } else {
          const resolution = resolveTypeScriptResultTypeForPostgresType(column, generatorConfig.codecProfile)
          collectTypeResolutionDependencies(typeDependencies, resolution)
          tsType = resolution.type
        }
      }
      return {
        ...(jsonShape ? { jsonShape } : {}),
        ...(jsonMapping ? { jsonMapping } : {}),
        name,
        nullable: resultNullabilityAllowsNull(column.nullability),
        pgType: column.pgType,
        pgTypeName: column.pgTypeName,
        pgTypeOid: column.pgTypeOid,
        pgTypeSchema: column.pgTypeSchema,
        propertyName,
        source: column.source,
        tsType,
      }
    })
    assertUniquePublicNames(
      columns.map((column) => column.propertyName),
      'result column',
      config.sourceFile
    )
    await assertColumnAssertions(client, config, columns)
    const parameters = config.parameters.map((parameter, paramIndex): ResolvedSqlParameter => {
      const analyzed = ir.params[paramIndex]
      if (!analyzed) {
        throw new Error(`${config.sourceFile}: analyzer returned no type for parameter ${parameter.name}.`)
      }

      const resolution =
        analyzed.checkConstraintType &&
        postgresParameterSupportsStringLiteralRefinement(analyzed, generatorConfig.codecProfile)
          ? {
              ambientBindings: [],
              scalarImports: [],
              type: tsTypeForCheckConstraintType(analyzed.checkConstraintType),
            }
          : resolveTypeScriptParameterTypeForPostgresType(analyzed, generatorConfig.codecProfile)
      collectTypeResolutionDependencies(typeDependencies, resolution)
      if (parameter.nullable === true && analyzed.nullAdmission === 'rejects') {
        throw new Error(
          `${config.sourceFile}: @param ${parameter.name} cannot be nullable because PostgreSQL proves that one of its uses rejects NULL.`
        )
      }
      return {
        name: parameter.name,
        nullable: analyzed.nullAdmission === 'accepts' || parameter.nullable === true,
        pgType: analyzed.pgType,
        pgTypeName: analyzed.pgTypeName,
        pgTypeSchema: analyzed.pgTypeSchema,
        propertyName: parameter.propertyName,
        tsType: resolution.type,
      }
    })
    const usesRecordUtilityType =
      parameters.length === 0 || columns.length === 0 || generatedDeclarations.some(({ fields }) => fields.length === 0)
    const ambientBindings = new Set(typeDependencies.ambient)
    if (usesRecordUtilityType) {
      ambientBindings.add('Record')
    }
    assertUniqueTypeScriptBindings(
      [
        { name: 'createTypedSqlStatement', source: 'generated runtime import' },
        { name: config.name, source: 'exported statement constant' },
        ...[...usedGeneratedTypeNames].map(([name, source]) => ({ name, source })),
        ...[...ambientBindings].map((name) => ({ name, source: 'ambient TypeScript type' })),
        ...[...typeDependencies.scalar].map((name) => ({
          name,
          source: 'postgres-typed-sql scalar type import',
        })),
      ],
      config.sourceFile
    )

    const inferredAccess = typedSqlAccessFromEvidence(ir.accessEvidence)
    if (config.access === 'read' && inferredAccess === 'write') {
      const reasons =
        ir.accessEvidence.kind === 'notProvenReadOnly'
          ? ir.accessEvidence.reasons.map(accessConcernDescription).join('; ')
          : 'an unknown analyzer result'
      throw new Error(
        `${config.sourceFile}: @access read requires analyzer-proved read-only execution; PostgreSQL analysis found ${reasons}.`
      )
    }

    resolvedQueries.push({
      columns,
      access: config.access ?? inferredAccess,
      cardinality: typedSqlCardinalityFromRowBounds(ir.rowBounds),
      command: typedSqlCommandFromPostgres(ir.command),
      generatedTypeDeclarations: generatedDeclarations.map(renderGeneratedTypeDeclaration),
      name: config.name,
      outputPath: config.outputPath,
      parameters,
      rowBounds: ir.rowBounds,
      scalarTypeImports: [...typeDependencies.scalar].toSorted(),
      sourceFile: config.sourceFile,
      sourcePath: config.sourcePath,
      sql: config.sql,
    })
  }

  return resolvedQueries
}

async function findGeneratedTypedSqlFiles(config: ResolvedPostgresTypedSqlConfig): Promise<string[]> {
  const generatedFiles = new Map<string, string>()
  async function walk(directory: string): Promise<void> {
    const entries = await readdir(directory, { withFileTypes: true })
    for (const entry of entries) {
      const entryPath = join(directory, entry.name)
      if (entry.isDirectory()) {
        if (!ignoredDiscoveryDirectories.has(entry.name) && !entry.name.startsWith('.')) {
          await walk(entryPath)
        }
        continue
      }

      if (
        entry.isFile() &&
        (entry.name.endsWith(typedSqlOutputSuffix) ||
          legacyTypedSqlOutputSuffixes.some((suffix) => entry.name.endsWith(suffix)))
      ) {
        const canonical = await realpath(entryPath)
        if (!generatedFiles.has(canonical)) {
          generatedFiles.set(canonical, entryPath)
        }
      }
    }
  }

  const includeRoots = new Map<string, string>()
  for (const includeRoot of config.include) {
    const canonical = await realpath(includeRoot)
    if (!includeRoots.has(canonical)) {
      includeRoots.set(canonical, includeRoot)
    }
  }
  for (const includeRoot of includeRoots.values()) {
    await walk(includeRoot)
  }
  return [...generatedFiles.values()].toSorted()
}

async function findStaleGeneratedFiles(
  configs: readonly ResolvedTypedSql[],
  generatorConfig: ResolvedPostgresTypedSqlConfig
): Promise<readonly string[]> {
  const expectedOutputPaths = new Set(
    await Promise.all(
      [generatorConfig.typesOutput, ...configs.map((config) => config.outputPath)].map(filesystemPathIdentity)
    )
  )
  const stale: string[] = []
  for (const generatedPath of await findGeneratedTypedSqlFiles(generatorConfig)) {
    if (expectedOutputPaths.has(await filesystemPathIdentity(generatedPath))) {
      continue
    }

    const contents = await readFile(generatedPath, 'utf8')
    if (
      contents !== generatedMarkerLine &&
      !contents.startsWith(`${generatedMarkerLine}\n`) &&
      !contents.startsWith(`${generatedMarkerLine}\r\n`)
    ) {
      continue
    }

    stale.push(generatedPath)
  }
  return stale
}

interface GeneratedOutput {
  readonly contents: string
  readonly outputPath: string
}

interface StagedGeneratedOutput extends GeneratedOutput {
  readonly backupPath: string
  readonly existed: boolean
  readonly stagedPath: string
}

interface StagedGeneratedDeletion {
  readonly backupPath: string
  readonly outputPath: string
}

function isMissingFileError(error: unknown): boolean {
  return error instanceof Error && 'code' in error && error.code === 'ENOENT'
}

function isAlreadyExistsError(error: unknown): boolean {
  return error instanceof Error && 'code' in error && error.code === 'EEXIST'
}

async function existingContents(outputPath: string): Promise<string | null> {
  try {
    return await readFile(outputPath, 'utf8')
  } catch (error) {
    if (isMissingFileError(error)) {
      return null
    }
    throw error
  }
}

async function assertOutputIsNotSymbolicLink(outputPath: string): Promise<void> {
  try {
    const output = await lstat(outputPath)
    if (output.isSymbolicLink()) {
      throw new Error(`Generated output path ${outputPath} must not be a symbolic link.`)
    }
  } catch (error) {
    if (!isMissingFileError(error)) {
      throw error
    }
  }
}

async function canonicalFilesystemPath(path: string): Promise<string> {
  const missingSegments: string[] = []
  let existingAncestor = path

  while (true) {
    try {
      return join(await realpath(existingAncestor), ...missingSegments)
    } catch (error) {
      if (!isMissingFileError(error)) {
        throw error
      }
      const parent = dirname(existingAncestor)
      if (parent === existingAncestor) {
        throw error
      }
      missingSegments.unshift(basename(existingAncestor))
      existingAncestor = parent
    }
  }
}

async function filesystemPathIdentity(path: string): Promise<string> {
  return canonicalFilesystemPath(path)
}

async function cleanStagingFiles(
  staged: readonly StagedGeneratedOutput[],
  deletions: readonly StagedGeneratedDeletion[] = []
): Promise<void> {
  await Promise.allSettled(
    [
      ...staged.flatMap((entry) => [entry.stagedPath, entry.backupPath]),
      ...deletions.map((entry) => entry.backupPath),
    ].map((path) => rm(path, { force: true }))
  )
}

async function cleanStagedWrites(staged: readonly StagedGeneratedOutput[]): Promise<void> {
  await Promise.allSettled(staged.map((entry) => rm(entry.stagedPath, { force: true })))
}

async function rollbackGeneratedOutputs(
  installed: readonly StagedGeneratedOutput[],
  backedUp: readonly (StagedGeneratedDeletion | StagedGeneratedOutput)[]
): Promise<unknown[]> {
  const errors: unknown[] = []
  for (const entry of installed.toReversed()) {
    try {
      await rm(entry.outputPath, { force: true })
    } catch (error) {
      errors.push(error)
    }
  }
  for (const entry of backedUp.toReversed()) {
    try {
      await rename(entry.backupPath, entry.outputPath)
    } catch (error) {
      errors.push(error)
    }
  }
  return errors
}

async function commitGeneratedOutputs(
  outputs: readonly GeneratedOutput[],
  stalePaths: readonly string[]
): Promise<void> {
  const outputByPath = new Map<string, GeneratedOutput>()
  for (const output of outputs) {
    const identity = await filesystemPathIdentity(output.outputPath)
    const first = outputByPath.get(identity)
    if (first) {
      throw new Error(
        `Generated output paths ${first.outputPath} and ${output.outputPath} refer to the same filesystem destination, which is claimed more than once.`
      )
    }
    outputByPath.set(identity, output)
  }

  const orderedOutputs = [...outputByPath.values()].toSorted((left, right) =>
    left.outputPath.localeCompare(right.outputPath)
  )
  const staleByPath = new Map<string, string>()
  for (const stalePath of stalePaths) {
    const identity = await filesystemPathIdentity(stalePath)
    if (outputByPath.has(identity)) {
      throw new Error(`Generated output path ${stalePath} cannot be both installed and removed.`)
    }
    if (!staleByPath.has(identity)) {
      staleByPath.set(identity, stalePath)
    }
  }
  const orderedStalePaths = [...staleByPath.values()].toSorted()
  for (const output of orderedOutputs) {
    await assertOutputIsNotSymbolicLink(output.outputPath)
  }
  for (const stalePath of orderedStalePaths) {
    await assertOutputIsNotSymbolicLink(stalePath)
  }

  const staged: StagedGeneratedOutput[] = []
  const stagedByPath = new Map<string, StagedGeneratedOutput>()
  const batchNonce = `${process.pid}-${randomUUID()}`
  const deletions: StagedGeneratedDeletion[] = orderedStalePaths.map((outputPath) => ({
    backupPath: join(dirname(outputPath), `.${basename(outputPath)}.${process.pid}-${randomUUID()}.backup`),
    outputPath,
  }))
  try {
    for (const output of orderedOutputs) {
      const previous = await existingContents(output.outputPath)
      if (previous === output.contents) {
        continue
      }

      await mkdir(dirname(output.outputPath), { recursive: true })
      const stagedPath = join(dirname(output.outputPath), `.${basename(output.outputPath)}.${batchNonce}.staged`)
      const backupPath = join(dirname(output.outputPath), `.${basename(output.outputPath)}.${batchNonce}.backup`)
      try {
        await writeFile(stagedPath, output.contents, { flag: 'wx' })
      } catch (error) {
        if (isAlreadyExistsError(error)) {
          const first = stagedByPath.get(await filesystemPathIdentity(stagedPath))
          if (first) {
            throw new Error(
              `Generated output paths ${first.outputPath} and ${output.outputPath} refer to the same filesystem destination, which is claimed more than once.`
            )
          }
        }
        throw error
      }
      const entry = { ...output, backupPath, existed: previous !== null, stagedPath }
      staged.push(entry)
      stagedByPath.set(await filesystemPathIdentity(stagedPath), entry)
    }
  } catch (error) {
    await cleanStagingFiles(staged, deletions)
    throw error
  }

  const backedUp: (StagedGeneratedDeletion | StagedGeneratedOutput)[] = []
  const installed: StagedGeneratedOutput[] = []
  try {
    for (const entry of deletions) {
      await rename(entry.outputPath, entry.backupPath)
      backedUp.push(entry)
    }
    for (const entry of staged) {
      if (entry.existed) {
        await rename(entry.outputPath, entry.backupPath)
        backedUp.push(entry)
      }
    }
    for (const entry of staged) {
      await rename(entry.stagedPath, entry.outputPath)
      installed.push(entry)
    }
  } catch (error) {
    const rollbackErrors = await rollbackGeneratedOutputs(installed, backedUp)
    await cleanStagedWrites(staged)
    if (rollbackErrors.length > 0) {
      throw new AggregateError(
        [error, ...rollbackErrors],
        'Generated output commit failed and rollback was incomplete.'
      )
    }
    await cleanStagingFiles(staged, deletions)
    throw error
  }

  await cleanStagingFiles(staged, deletions)
}

export interface GenerateTypedSqlResult {
  readonly generatedFiles: readonly string[]
  readonly removedFiles: number
  readonly statementCount: number
}

export async function generateTypedSql(config: PostgresTypedSqlConfig): Promise<GenerateTypedSqlResult> {
  if (generationInProgress) {
    throw new Error('Concurrent generation in one process is not supported.')
  }

  generationInProgress = true
  let database: Awaited<ReturnType<typeof createAnalysisDatabase>> | undefined

  try {
    const resolvedConfig = resolveConfig(config)
    database = await createAnalysisDatabase({
      extensions: resolvedConfig.extensions,
      schemaFiles: resolvedConfig.schemaFiles,
    })
    const catalogContents = await buildCatalogTypes(database, resolvedConfig)
    const rawConfigs = await readTypedSqlConfigs(resolvedConfig)
    assertUniqueTypedSqlNames(rawConfigs)
    const configs = rawConfigs.map((config) => compileTypedSql(config, resolvedConfig.naming.parameterProperties))
    const resolvedConfigs = await resolveTypedSqlWithAnalyzer(configs, database, resolvedConfig)

    const staleFiles = await findStaleGeneratedFiles(resolvedConfigs, resolvedConfig)
    await commitGeneratedOutputs(
      [
        { contents: catalogContents, outputPath: resolvedConfig.typesOutput },
        ...resolvedConfigs.map((entry) => ({
          contents: renderTypedSql(entry, resolvedConfig),
          outputPath: entry.outputPath,
        })),
      ],
      staleFiles
    )
    return {
      generatedFiles: resolvedConfigs.map((entry) => entry.outputPath),
      removedFiles: staleFiles.length,
      statementCount: configs.length,
    }
  } finally {
    try {
      await database?.close()
    } finally {
      generationInProgress = false
    }
  }
}
